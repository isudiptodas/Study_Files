// Post decrement (First print the value then decrement the value by 1)

#include<iostream>
using namespace std;

int main(){
    
    int a = 6;
    int b = 18;
    
    cout<<"Before increment "<<endl;
    cout<<a<<" "<<b<<endl;
    
    a--;
    b--;
    
    cout<<"After increment "<<endl;
    cout<<a<<" "<<b<<endl;
    
    
}