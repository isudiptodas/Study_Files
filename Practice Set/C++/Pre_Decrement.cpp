// Pre decrement (First decrement the value by 1 then print the value)

#include<iostream>
using namespace std;

int main(){
    
    int a = 6;
    int b = 18;
    
    cout<<"Before decrement "<<endl;
    cout<<a<<" "<<b<<endl;
    
    --a;
    --b;
    
    cout<<"After decrement "<<endl;
    cout<<a<<" "<<b<<endl;
    
    
}
