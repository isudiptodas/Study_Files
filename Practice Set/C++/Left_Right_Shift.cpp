#include<iostream>
using namespace std;

int main(){
    
    /*
    right shift (>>) divides by 2 to 
    the power of the shift amount, and left shift (<<) 
    multiplies by 2 to the power of the shift amount.
    */

    cout<<(17>>1)<<endl;
    cout<<(17>>2)<<endl;
    cout<<(19>>1)<<endl;
    cout<<(19>>2)<<endl;
    
}