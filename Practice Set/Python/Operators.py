'''
• (//) Float division - Returns the value without floating part
• (**) Exponentiation - Returns a number multiplied by its power
  for example : 5**5 = 5x5x5 = 125
  
'''

a = 15
b = 10

print(a+b)    # Returns addition
print(a-b)    # Returns subtraction
print(a*b)    # Returns multiplication
print(a/b)    # Returns division
print(a//b)   # Returns division without point value
print(a%b)    # Returns the remainder
